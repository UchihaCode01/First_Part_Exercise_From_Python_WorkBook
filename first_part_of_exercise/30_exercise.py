
temperature = float(input("Enter the temperature in Celsius : "))

Celsius_in_Fahrenheit = temperature * 1.8 + 32
Celsius_in_Kelvin = temperature + 273.15

print("The temperature in Celsius is ", temperature)
print("The temperature in Fahrenheit is ", Celsius_in_Fahrenheit)
print("The temperature in Kelvin is ", Celsius_in_Kelvin)